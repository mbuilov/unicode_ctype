#include <stdio.h>
#include <stdlib.h>
#include <wctype.h>
#include <locale.h>

#define MAXDISTANCE 1000
#define MAXTABLES   20

static unsigned get_bits(unsigned i, const wctype_t t[12])
{
	unsigned w0 = !!iswctype(i, t[0]);
	unsigned w1 = !!iswctype(i, t[1]);
	unsigned w2 = !!iswctype(i, t[2]);
	unsigned w3 = !!iswctype(i, t[3]);
	unsigned w4 = !!iswctype(i, t[4]);
	unsigned w5 = !!iswctype(i, t[5]);
	unsigned w6 = !!iswctype(i, t[6]);
	unsigned w7 = !!iswctype(i, t[7]);
	unsigned w8 = !!iswctype(i, t[8]);
	unsigned w9 = !!iswctype(i, t[9]);
	unsigned w10 = !!iswctype(i, t[10]);
	unsigned w11 = !!iswctype(i, t[11]);
	unsigned bits = w0 | (w1 << 1) | (w2 << 2) | (w3 << 3) |
		(w4 << 4) | (w5 << 5) | (w6 << 6) | (w7 << 7) |
		(w8 << 8) | (w9 << 9) | (w10 << 10) | (w11 << 11);
	return bits;
}

static int subgen(const char *prefix, const wctype_t t[12],
	unsigned only, unsigned only_not, unsigned excpt1, unsigned excpt2)
{
	unsigned r = 0, d = 0, i = 0, s = 0, mask = 0;
	unsigned starts[MAXTABLES] = {0};
	for (; i <= 0x10FFFF; i++) {
		unsigned bits = get_bits(i, t);
		if (bits && (only ? (bits & only) == only && !(bits & ~only_not) : bits != excpt1 && bits != excpt2)) {
			if (bits & 1) {
				if (bits & ~0xFF)
					return -1;
			}
			else if (bits & 0x1F)
				return -1;
			if (r) {
				if (d > MAXDISTANCE) {
					if (mask) {
						printf("0x%.8x,\n", mask);
						mask = 0;
					}
					printf("};\n");
					r = 0;
					d = 0;
				}
				else if (only) {
					for (; d; d--, r++) {
						if (!(r & 31)) {
							printf("0x%.8x,\n", mask);
							mask = 0;
						}
					}
				}
				else for (; d; d--, r++) {
					if (!(r & 7)) {
						printf("0x%.8x,\n", mask);
						mask = 0;
					}
				}
			}
			if (!r) {
				printf("\nstatic const unsigned %s_%.6x[] = {\n", prefix, i);
				if (s == MAXTABLES)
					return -1;
				starts[s++] = i;
			}
			if (r && !(r & (only ? 31 : 7))) {
				printf("0x%.8x,\n", mask);
				mask = 0;
			}
			if (only)
				mask |= 1u << (r & 31);
			else {
				switch (bits & (
					(1 << 0) |
					(1 << 2) |
					(1 << 3) |
					(1 << 4) |
					(1 << 5) |
					(1 << 6) |
					(1 << 7) |
					(1 << 8) |
					(1 << 9) |
					(1 << 11)))
				{
					case (1 << 0) | (1 << 5) | (1 << 6):                        bits = 1; break;
					case (1 << 0) | (1 << 5) | (1 << 6) | (1 << 2):             bits = 2; break;
					case (1 << 0) | (1 << 5) | (1 << 6) | (1 << 3):             bits = 3; break;
					case (1 << 0) | (1 << 5) | (1 << 6) | (1 << 2) | (1 << 3):  bits = 4; break;
					case (1 << 0) | (1 << 5) | (1 << 6) | (1 << 4) | (1 << 7):  bits = 5; break;
					case (1 << 0) | (1 << 5) | (1 << 6) | (1 << 2) | (1 << 4):  bits = 6; break;
					case (1 << 0) | (1 << 5) | (1 << 6) | (1 << 3) | (1 << 4):  bits = 7; break;
					case (1 << 8) | (1 << 11):                                  bits = 8; break;
					case (1 << 6) | (1 << 8) | (1 << 11):                       bits = 9; break;
					case (1 << 9):                                              bits = 10; break;
					case (1 << 9) | (1 << 11):                                  bits = 11; break;
					case (1 << 8) | (1 << 9) | (1 << 11):                       bits = 12; break;
					default:
						printf("bits: %x\n", bits);
						return -1;
				}
				mask |= bits << 4*(r & 7);
			}
			r++;
		}
		else if (r)
			d++;
	}
	if (r) {
		if (mask)
			printf("0x%.8x,\n", mask);
		printf("};\n");
	}
	if (!only && starts[0])
		return -1;
	if (!only)
		printf("\nstatic unsigned %s(const unsigned w, const int t)\n", prefix);
	else
		printf("\nstatic unsigned %s(const unsigned w)\n", prefix);
	printf("{\n\tstatic const unsigned starts[] = {\n");
	for (i = 0; i < s; i++)
		printf("\t\t0x%.6x,\n", starts[i]);
	printf("\t};\n");
	printf("\tstatic const unsigned sizes[] = {\n");
	for (i = 0; i < s; i++)
		printf("\t\tsizeof(%s_%.6x)/sizeof(%s_%.6x[0]),\n", prefix, starts[i], prefix, starts[i]);
	printf("\t};\n");
	printf("\tstatic const unsigned *const tables[] = {\n");
	for (i = 0; i < s; i++)
		printf("\t\t%s_%.6x,\n", prefix, starts[i]);
	printf("\t};\n");
	if (!only)
		printf("\tstatic const unsigned bit_map[] = {\n"
			"\t\t0,\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6)),\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6) | (1u << 2)),\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6) | (1u << 3)),\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6) | (1u << 2) | (1u << 3)),\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6) | (1u << 4) | (1u << 7)),\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6) | (1u << 2) | (1u << 4)),\n"
			"\t\t((1u << 0) | (1u << 5) | (1u << 6) | (1u << 3) | (1u << 4)),\n"
			"\t\t((1u << 8) | (1u << 11)),\n"
			"\t\t((1u << 6) | (1u << 8) | (1u << 11)),\n"
			"\t\t(1u << 9),\n"
			"\t\t((1u << 9) | (1u << 11)),\n"
			"\t\t((1u << 8) | (1u << 9) | (1u << 11)),\n"
			"\t\t0,\n"
			"\t\t0,\n"
			"\t\t0\n"
			"\t};\n");
	printf("\tif (w < starts[1])\n");
	if (only)
		printf("\t\treturn w >= starts[0] && (w - starts[0])/(8*sizeof(*tables[0])) < sizes[0] ?\n"
			"\t\t\ttables[0][(w - starts[0])/(8*sizeof(*tables[0]))] &\n"
			"\t\t\t\t(1u << (w - starts[0])%%(8*sizeof(*tables[0]))) : 0;\n");
	else
		printf("\t\treturn w/(2*sizeof(*tables[0])) < sizes[0] ? (1u << t) &\n"
			"\t\t\tbit_map[15u & (tables[0][w/(2*sizeof(*tables[0]))] >> 4*(w%%(2*sizeof(*tables[0]))))] : 0;\n");
	printf("\t{\n");
	printf("\t\tunsigned i = sizeof(starts)/sizeof(starts[0]);\n");
	printf("\t\twhile (w < starts[--i]);\n");
	if (only)
		printf("\t\treturn (w - starts[i])/(8*sizeof(*tables[0])) < sizes[i] ?\n"
			"\t\t\ttables[i][(w - starts[i])/(8*sizeof(*tables[0]))] &\n"
			"\t\t\t\t(1u << (w - starts[i])%%(8*sizeof(*tables[0]))) : 0;\n");
	else
		printf("\t\treturn (w - starts[i])/(2*sizeof(*tables[0])) < sizes[i] ? (1u << t) &\n"
			"\t\t\tbit_map[15u & (tables[i][(w - starts[i])/(2*sizeof(*tables[0]))] >> 4*((w - starts[i])%%(2*sizeof(*tables[0]))))] : 0;\n");
	printf("\t}\n");
	printf("}\n");
	return 0;
}


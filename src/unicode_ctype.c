int main(int argc, char *argv[])
{
	unsigned r = 0;
	unsigned i = 0;
	wctype_t t1[12];
	unsigned t2[12];
	setlocale(LC_ALL, "");
	t1[0] = wctype("alnum");
	t1[1] = wctype("alpha");
	t1[2] = wctype("lower");
	t1[3] = wctype("upper");
	t1[4] = wctype("xdigit");
	t1[5] = wctype("graph");
	t1[6] = wctype("print");
	t1[7] = wctype("digit");
	t1[8] = wctype("blank");
	t1[9] = wctype("cntrl");
	t1[10] = wctype("punct");
	t1[11] = wctype("space");
	t2[0] = unicode_ctype("alnum");
	t2[1] = unicode_ctype("alpha");
	t2[2] = unicode_ctype("lower");
	t2[3] = unicode_ctype("upper");
	t2[4] = unicode_ctype("xdigit");
	t2[5] = unicode_ctype("graph");
	t2[6] = unicode_ctype("print");
	t2[7] = unicode_ctype("digit");
	t2[8] = unicode_ctype("blank");
	t2[9] = unicode_ctype("cntrl");
	t2[10] = unicode_ctype("punct");
	t2[11] = unicode_ctype("space");
	for (; i <= 0x10FFFF; i++) {
		unsigned bits1 = get_bits1(i, t1);
		unsigned bits2 = get_bits2(i, t2);
		int alnum = !!(bits1 & (1 << 0));
		int alpha = !!(bits1 & (1 << 1));
		int lower = !!(bits1 & (1 << 2));
		int upper = !!(bits1 & (1 << 3));
		int xdigit = !!(bits1 & (1 << 4));
		int graph = !!(bits1 & (1 << 5));
		int print = !!(bits1 & (1 << 6));
		int digit = !!(bits1 & (1 << 7));
		int blank = !!(bits1 & (1 << 8));
		int cntrl = !!(bits1 & (1 << 9));
		int punct = !!(bits1 & (1 << 0));
		int space = !!(bits1 & (1 << 11));
		if (bits1 != bits2)
			printf("%x: %x != %x\n", i, bits1, bits2);
		r += bits1;
		r += bits2;
#define CHECKNOT(bad_expr) if (bad_expr) return printf("%u:" #bad_expr "\n", i), 2
		/* "alnum":  E "graph", "alpha" U "digit" */
		/* "alpha":  E "alnum" */
		CHECKNOT(alpha && !alnum);
		/* "lower":  E "alpha", may be also "upper" */
		CHECKNOT(lower && !alpha);
		/* "upper":  E "alpha", may be also "lower" */
		CHECKNOT(upper && !alpha);
		/* "xdigit": E "alnum", "alpha" (['a'..'f'] U ['A'..'F']) U "digit" (['0'..'9']) */
		CHECKNOT(xdigit && !alnum);
		/* "graph":  E "print" */
		CHECKNOT(graph && !print);
		/* "print": !E "cntrl" */
		CHECKNOT(print && cntrl);
		/* "digit":  E "xdigit", ['0'..'9'] */
		CHECKNOT(digit && !xdigit);
		/* "blank": E "space", may be also "print", example: '\t' (not "print"), ' ' (also "print") */
		CHECKNOT(blank && !space);
		CHECKNOT(('\t' == i || ' ' == i) && !blank);
		CHECKNOT('\t' == i && print);
		CHECKNOT(' ' == i && !print);
		/* "cntrl": !E "print", example: '\t' (also "blank"), but not ' ' (also "blank") */
		CHECKNOT(cntrl && print);
		CHECKNOT('\t' == i && !cntrl);
		CHECKNOT(' ' == i && cntrl);
		/* "punct": E "graph", !E "space", !E "cntrl", example: '0' (also "digit") */
		CHECKNOT(punct && !graph);
		CHECKNOT(punct && (space || cntrl));
		CHECKNOT('0' == i && (!punct || !digit));
		/* "space": !E "graph", example (not a "blank"): '\n', '\f' */
		CHECKNOT(space && graph);
		CHECKNOT(('\n' == i || '\f' == i) && (!space || blank));
		/*   alnum ? lower upper xdigit digit (+graph +print)
		 *   !alnum ? print blank cntrl space
		 *  12 combinations:
		 *   alnum
		 *   alnum lower
		 *   alnum       upper
		 *   alnum lower upper
		 *   alnum             xdigit digit
		 *   alnum lower       xdigit
		 *   alnum       upper xdigit
		 *   !alnum       blank       space
		 *   !alnum print blank       space
		 *   !alnum             cntrl
		 *   !alnum             cntrl space
		 *   !alnum       blank cntrl space */
		CHECKNOT(alnum && (!graph || !print));
		CHECKNOT(lower && upper && xdigit);
		CHECKNOT((lower || upper) && digit);
		CHECKNOT(digit && !xdigit);
		CHECKNOT(space && !blank && !cntrl);
		CHECKNOT(blank && space && graph);
#undef CHECKNOT
	}
	return r & 255;
}
